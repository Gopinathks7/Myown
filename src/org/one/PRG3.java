package org.one;

public class PRG3 {

	public static void main(String[] args) 
	{
		int a=10;
		int b=20;
		int temp=0;
		

		System.out.println("Before swaping  A is "+a);
		System.out.println("Before swaping  B is "+b);
		temp=a;
		a=b;
		b=temp;
		System.out.println("After swaping A is "+a);
		System.out.println("After swaping  B is "+b);
	}

}
